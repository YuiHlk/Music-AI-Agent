package com.musicai.agent.application;

/**
 * 定义生成规则可表达的整体音乐情绪。
 */
public enum MusicalMood {
    /** 阴暗氛围，倾向小调与较沉重的音高材料。 */
    DARK,
    /** 明亮氛围，倾向大调及较高音区变化。 */
    BRIGHT,
    /** 强烈进攻感，减少休止并保持紧凑推进。 */
    AGGRESSIVE,
    /** 忧郁氛围，以小调色彩和适度留白为主。 */
    MELANCHOLIC,
    /** 充满能量的氛围，强调连续节奏推进。 */
    ENERGETIC,
    /** 平静氛围，通过更多休止营造呼吸感。 */
    CALM
}
